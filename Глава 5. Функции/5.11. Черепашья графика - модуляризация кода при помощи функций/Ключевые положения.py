import Modturtle as M_t

def main():
    M_t.square(100, 0, 50, 'red')
    M_t.square(-150, -100, 200, 'blue')
    M_t.square(-200, 150, 75, 'green')

    M_t.cirle(0, 0, 100, 'red')
    M_t.cirle(-150, -75, 200, 'blue')
    M_t.cirle(-200, 150, 75, 'green')
    M_t.t.done()
if __name__ == '__main__': main()
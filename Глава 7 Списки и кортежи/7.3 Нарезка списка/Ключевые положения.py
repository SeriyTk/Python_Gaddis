# Срез -  это диапазон элементов, которые извлекаются из последовательности .
# Для того что бы получить срез списка, пишут выражение в приведенном ниже общем формате:
# имя_списка [ начало : конец ]
days = ['понедельник',"вторник","среда","четверг","пятница","суббота","воскресенье"]
print(days[2:5])
